#ifndef RANDOM_ACCESS_ITERATOR_TAG_HPP
# define RANDOM_ACCESS_ITERATOR_TAG_HPP

# include "utils.hpp"

namespace ft
{
	/*==============================================================================*/
	/*-----------------------------ITERATOR TAGS STRUCTS----------------------------*/
	/*==============================================================================*/
	/* The iterator tags are empty structs whose only goal is to identify the type  */
	/* of the iterator. You have 5 types of iterators with different capabilities.  */
	/* They inherit from the first and the most useful of them is the random access */
	/* is the random_access_iterator because it can do everything the others can do */
	/* since it inherit from all of them.											*/
	/*------------------------------------------------------------------------------*/

	/*==============================================================================*/
	/*---------------------------INPUT ITERATOR TAG STRUCT--------------------------*/
	/*==============================================================================*/
	struct input_iterator_tag
	{
	};
	/*==============================================================================*/

	/*==============================================================================*/
	/*---------------------------OUTPUT ITERATOR TAG STRUCT-------------------------*/
	/*==============================================================================*/
	struct output_iterator_tag
	{
	};
	/*==============================================================================*/

	/*==============================================================================*/
	/*--------------------------FORWARD ITERATOR TAG STRUCT-------------------------*/
	/*==============================================================================*/
	struct forward_iterator_tag : public input_iterator_tag
	{
	};
	/*==============================================================================*/

	/*==============================================================================*/
	/*-----------------------BIDIRECTIONAL ITERATOR TAG STRUCT----------------------*/
	/*==============================================================================*/
	struct bidirectional_iterator_tag : public forward_iterator_tag
	{
	};
	/*==============================================================================*/

	/*==============================================================================*/
	/*-----------------------RANDOM ACCESS ITERATOR TAG STRUCT----------------------*/
	/*==============================================================================*/
	struct random_access_iterator_tag : public bidirectional_iterator_tag
	{
	};
}

#endif